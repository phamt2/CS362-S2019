char *inputString()
{
    // TODO: rewrite this function
    //Generates random characters starting from lower case a ending with lower case z to decrease the number of combinations and sticks them into a static string array so we can actually return the string array and it was initialized to 5 so that the 6th letter call would be a null since it does not exist in a 5 letter string.

    static char randomString[5];
    for (int a = 0; a < 5; a++)
    {
        char randomLetter = 97 + rand() % 26 ;
        randomString[a] = randomLetter;
    };
        
    return randomString;
}

